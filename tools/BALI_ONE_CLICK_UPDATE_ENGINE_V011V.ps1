param(











  [Parameter(Mandatory=$true)][string]$Root,











  [Parameter(Mandatory=$true)][string]$Report,











  [Parameter(Mandatory=$true)][string]$Stamp











)























$ErrorActionPreference = 'Continue'











$rootPath = [System.IO.Path]::GetFullPath($Root).TrimEnd('\')











