Add-Report ''























$valid = @()











foreach($f in $rawFiles) {











  $zip = $null











  try {











    if([string]::IsNullOrWhiteSpace($f.FullName) -or -not (Test-Path -LiteralPath $f.FullName -PathType Leaf)) {











      Add-Report ('SKIP invalid file path: ' + [string]$f.FullName)











      continue











    }











    $zip = [System.IO.Compression.ZipFile]::OpenRead($f.FullName)











    $entry = $zip.Entries | Where-Object { $_.FullName -ieq 'BALI_PATCH_MANIFEST.txt' } | Select-Object -First 1











    if(-not $entry) { Add-Report ('SKIP no manifest: ' + $f.FullName); continue }











    $reader = New-Object System.IO.StreamReader($entry.Open())











    $text = $reader.ReadToEnd()











    $reader.Close()











    if($text -notmatch '(?im)^BALI_PATCH_MANIFEST=1\s*$') { Add-Report ('SKIP invalid manifest marker: ' + $f.FullName); continue }











    if($text -notmatch '(?im)^PROJECT=BALI_ROCKET_CRYPTO_COMMAND\s*$') { Add-Report ('SKIP wrong project manifest: ' + $f.FullName); continue }











    if($text -match '(?im)^LIVE_ORDERS=ON\s*$') { Add-Report ('SKIP unsafe live-orders manifest: ' + $f.FullName); continue }











    if($text -match '(?im)^API_KEYS_TOUCHED=YES\s*$') { Add-Report ('SKIP API-key touching manifest: ' + $f.FullName); continue }











    $valid += [pscustomobject]@{











      File=$f











      Manifest=$text











      Version=Get-VersionText $text











      VersionRank=Get-VersionRank $text











      LastWriteTime=$f.LastWriteTime











    }











  } catch {











    Add-Report ('SKIP zip read failed: ' + $f.FullName + ' :: ' + $_.Exception.Message)











  } finally {











    if($zip) { $zip.Dispose() }











  }











}























