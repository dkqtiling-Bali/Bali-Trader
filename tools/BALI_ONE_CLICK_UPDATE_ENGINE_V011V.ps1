function Add-Report([string]$Text='') {











  Add-Content -LiteralPath $Report -Value $Text -Encoding ASCII











}











function Get-VersionRank([string]$Text) {











  $m = [regex]::Match($Text, '(?im)^VERSION=V(\d+)([A-Z])\s*$')











  if(-not $m.Success) { return 0 }











  $num = [int]$m.Groups[1].Value











  $letter = $m.Groups[2].Value.ToUpper()[0]











  $letterRank = [int][char]$letter - [int][char]'A'











  return ($num * 100) + $letterRank











}











function Get-VersionText([string]$Text) {











  $m = [regex]::Match($Text, '(?im)^VERSION=(V\d+[A-Z])\s*$')











  if($m.Success) { return $m.Groups[1].Value }











  return 'UNKNOWN'











}











function Copy-StageItem($Item, [string]$DestRoot) {











  $dest = Join-Path $DestRoot $Item.Name











  if($Item.PSIsContainer) {











    New-Item -ItemType Directory -Force -Path $dest | Out-Null











    $children = Get-ChildItem -LiteralPath $Item.FullName -Force -ErrorAction Stop











    foreach($child in $children) {











      Copy-Item -LiteralPath $child.FullName -Destination $dest -Recurse -Force -ErrorAction Stop











    }











  } else {











    Copy-Item -LiteralPath $Item.FullName -Destination $dest -Force -ErrorAction Stop











  }











}























