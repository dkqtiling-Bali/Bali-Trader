$dirs = @($updates, (Join-Path $env:USERPROFILE 'Downloads'), (Join-Path $env:USERPROFILE 'Desktop'), $rootPath) | Select-Object -Unique











$rawFiles = foreach($d in $dirs) {











  if(Test-Path -LiteralPath $d) {











    Get-ChildItem -LiteralPath $d -Filter 'BALI_ROCKET_CRYPTO_COMMAND_*.zip' -File -ErrorAction SilentlyContinue











  }











}











